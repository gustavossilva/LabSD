    private void btnCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCadastrarMouseClicked
        try {
            String cidade = txtCidade.getText();
            String name = txtNome.getText();
            int id = 0;
            try{
                id = Integer.parseInt(txtIdreg.getText());
            }catch(NumberFormatException e) {
                System.out.println(e);
            }
            transport = new TSocket("localhost", 25000);
            transport.open();

            TProtocol protocol = new TBinaryProtocol(transport);
            //Protocolo para utilizar concorrÃªncia com apenas 1 thread
            //TProtocol protocol = new TBinaryProtocol(new TFramedTransport(transport));
            Operations.Client client = new Operations.Client(protocol);
            boolean entrar = client.criarVertice(id,0,cidade,0,name);
            if(entrar){
                JOptionPane.showMessageDialog(null,"Registrado com sucesso");
                this.setVisible(false);
                login.setVisible(true);
                transport.close();
            }else{
                JOptionPane.showMessageDialog(null,"Falha no registro!");
            }

        } catch (TException x) {
            JOptionPane.showMessageDialog(null,x);

        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnCadastrarMouseClicked

